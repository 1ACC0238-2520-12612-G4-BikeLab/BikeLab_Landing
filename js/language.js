function changeLanguage(lang) {
    console.log('Cambiando idioma a:', lang);
    
    const elements = document.querySelectorAll('[id]');
    elements.forEach(element => {
        const id = element.id;
        if (translations[lang] && translations[lang][id]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {

                const placeholderKey = id + '-placeholder';
                if (translations[lang][placeholderKey]) {
                    element.placeholder = translations[lang][placeholderKey];
                }
            } else if (element.tagName === 'OPTION') {

                element.textContent = translations[lang][id];
            } else if (element.tagName === 'LABEL') {

                element.textContent = translations[lang][id];
            } else {

                element.textContent = translations[lang][id];
            }
        }
    });
    

    updatePlaceholders(lang);
    
    localStorage.setItem('preferred-language', lang);
}


function updatePlaceholders(lang) {
    const placeholders = {
        'user-name': 'name-placeholder',
        'user-email': 'email-placeholder', 
        'user-phone': 'phone-placeholder',
        'other-university': 'other-university-placeholder'
    };
    
    Object.keys(placeholders).forEach(inputId => {
        const input = document.getElementById(inputId);
        const placeholderKey = placeholders[inputId];
        
        if (input && translations[lang] && translations[lang][placeholderKey]) {
            input.placeholder = translations[lang][placeholderKey];
        }
    });
}


document.addEventListener('DOMContentLoaded', function() {
    const languageSelector = document.getElementById('language-selector');
    
    if (languageSelector) {

        const savedLang = localStorage.getItem('preferred-language') || 'es';
        languageSelector.value = savedLang;
        

        changeLanguage(savedLang);
        

        languageSelector.addEventListener('change', function() {
            changeLanguage(this.value);
        });
    }
});


window.changeLanguage = changeLanguage;
